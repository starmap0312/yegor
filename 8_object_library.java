// Library Oriented Architecture (LOA)
// 1) it is a set of principles and methodologies for designing and developing software in the form of reusable libraries
// 2) it dictates the boundaries of a library by exposing functionality through a set of public interfaces (APIs)
// 3) it promotes separation of concerns and programming to interfaces not implementations
// 4) it promotes practices similar to Modular Programming and encourages the maintenance of internal libraries
//
// principles of LOA
// 1) a software library implementation must be constrained to only one domain
// 2) all domain specific software libraries must be maintained and supported with separate life-cycles
// 3) a software library that needs to use concepts from a different domain than the one it belongs to
//    must use/interact with the library corresponding to that domain
//
