// Library Oriented Architecture (LOA)
// 1) a set of principles and methodologies for developing software in the form of reusable libraries
// 2) it dictates the boundaries of a library by exposing functionality through public interfaces (APIs)
// 3) it promotes separation of concerns and programming to interfaces not implementations
// 4) it promotes practices similar to Modular Programming
//    it encourages the maintenance of internal libraries

// principles of LOA
// 1) a software library implementation must be constrained to only one domain
//    so a software library that needs to use concepts from a different domain 
//      must interact with the library of that domain
// 2) all domain specific software libraries must be maintained with separate life-cycles

